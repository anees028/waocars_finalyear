 <script>
  function validateNumber(evt) {
    var ch = String.fromCharCode(evt.which);
    if (!(/[0-9]/.test(ch))) {
      evt.preventDefault();
    }
  }

  function validateString(evt) {
    var ch = String.fromCharCode(evt.which);
    if (!(/[a-zA-Z]/.test(ch))) {
      evt.preventDefault();
    }
  }

  function validateAlphanumeric(evt) {
    var ch = String.fromCharCode(evt.which);
    if (!(/[a-zA-Z0-9_. ]/.test(ch))) {
      evt.preventDefault();
    }
  }

  function validateEmail(evt) {
    var ch = String.fromCharCode(evt.which);
    if (!(/[a-zA-Z0-9@_.]/.test(ch))) {
      evt.preventDefault();
    }
  } 



  
